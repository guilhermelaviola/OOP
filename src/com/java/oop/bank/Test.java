    private void jButton1ActionPerformed(ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
    	client.bankNumber = Integer.parseInt(bankNumberTxt.getText());
    	client.agencyNumber = Integer.parseInt(agencyNumberTxt.getText());
    	client.balance=0;
    	client.name=nameTxt.getText();
    	client.idNumber = idTxt.getText();
        output.setText("Client registered successfully.");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        double value = Double.parseDouble(valueTxt.getText());
        double formerBalance= client.balance;
        client.withdraw(value);
        double currentBalance = client.balance;
        if(formerBalance<currentBalance){
            output.setText("You withdrew "+ value + " succesfully!");
        }
        else{
            output.setText("You don't have enough cash to perform this operation!");
        }
        valueTxt.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        double value=Double.parseDouble(valueTxt.getText());
        client.balance=client.balance+value;
        valueTxt.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

